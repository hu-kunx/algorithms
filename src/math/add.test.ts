import { decimalBigNumberAdd as add } from './add';

describe('Add', () => {
  test('add("1",2323) === "2324"', () => {
    expect(add('1', 2323)).toBe('2324');
  });

  test('add("1","23423") === "23424"', () => {
    expect(add('1', '23423')).toBe('23424');
  });

  test('add("10000000000000000000000001",2323) === "10000000000000000000002324"', () => {
    expect(add('10000000000000000000000001', 2323)).toBe(
      '10000000000000000000002324'
    );
  });

  test('add("10000000000000000000000001","10000000000000000000000001) === "20000000000000000000000002"', () => {
    expect(
      add('10000000000000000000000001', '10000000000000000000000001')
    ).toBe('20000000000000000000000002');
  });

  test('add(2324,2323) === "4646"', () => {
    expect(add(2324, 2323)).toBe('4647');
  });
});
